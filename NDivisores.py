def divisors(NumDivisors):
    count = 1
    Resultado = 0
    while count <= NumDivisors:
      if NumDivisors % count == 0:
      	Resultado += 1
      count = count +1
    return Resultado
    
    
print(divisors(0))
print(divisors(-1))
print(divisors(12))
print(divisors(128))
print(divisors(1200))
print(divisors(7000))